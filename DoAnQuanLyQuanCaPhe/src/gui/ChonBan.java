    private boolean kiemTraBan(int BanID)
    {
        boolean kq =false;
         for (int i = 0; i < dtm2.getRowCount(); i++) {
             // Duyệt qua mỗi cột trên dòng hiện tại

                 // Lấy giá trị tại dòng i, cột j
                 int banID = Integer.parseInt(dtm2.getValueAt(i, 0).toString());
                 
                 if(banID==BanID)
                 {
                     kq=true;
                     break;
                 }
        }
        
        
        return kq;
    }
