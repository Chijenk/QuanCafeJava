    private void btnThucThiGopBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThucThiGopBanActionPerformed
        // TODO add your handling code here:
        dsBan=BanDAO.layDanhSachBan();
        // Thực hiện logic gộp bàn tại đây
        if (dsBanDuocChon.size() < 2) {
            JOptionPane.showMessageDialog(null, "Chọn ít nhất 2 bàn để gộp!");
            return;
        }
        
        // Xử lý logic gộp bàn, ví dụ: tạo một bàn mới với số ghế là tổng số ghế của các bàn được chọn
        int tongSoGhe = 0;
        for (String banID : dsBanDuocChon) {
            for (Ban ban : dsBan) {
                String ban_id=String.valueOf(ban.getBanID());
                if (ban_id.equals(banID)) {
                    tongSoGhe += ban.getSoGhe();
                    // Xóa bàn đã được chọn từ danh sách (hoặc bạn có thể đánh dấu là đã gộp)
                    BanDAO.capNhaTrangThaiBanGop(ban.getBanID());
                    break;
                }
            }
        }
        
        // Giả lập tạo bàn mới
        
        int ban_id_lon_nhat=BanDAO.layMaMonLonNhat()+1;
        Ban banMoi = new Ban(ban_id_lon_nhat,"Bàn "+String.valueOf(ban_id_lon_nhat),"Có khách",tongSoGhe);
        
        // Thêm bàn mới vào danh sách
        BanDAO.themBan(banMoi);
        
        // Làm mới giao diện để hiển thị bàn vừa gộp
        
        dsBanDuocChon.clear();
        JOptionPane.showMessageDialog(null, "Đã gộp bàn thành công!");
        
        
        
        
    
    }//GEN-LAST:event_btnThucThiGopBanActionPerformed

